    <h1>Desafio 5</h1>
    <a href="/vaga/Desafio_TargetSistemas">Voltar</a>
    <hr>
    <p>Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?</p><br>

    <hr>

    <h4>Resposta:</h4>

    <p>PASSO 1: Descobriria primeiro o interrupitor que acende a lampada da sala que estou. Descobrindo qual acende a minha sala, numeraria ele como SALA 1</p>
    <p>PASSO 2: Acionaria o proximo interrupitor e me dirigiria a sala 2 e verificaria se a lamapada estava acesa. Voltaria para a sala 1 e, caso a lampada da sala 2 estivece acesa, numeraria aquele interrupitor como SALA 2</p>
